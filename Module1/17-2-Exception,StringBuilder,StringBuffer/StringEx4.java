package com.a172;

public class StringEx4 
{
	public static void main(String[] args) {
		
		//literal String
		String s1 ="sachin";
		
		//new keyword String
		String s2 =new String("sachin");
		
		//character array String
		char ch[]= {'s','a','c','h','i','n'};
		String s3 =new String(ch);
		
		System.out.println(s1);
		System.out.println(s2);
		System.out.println(s3);
		
	}
}
